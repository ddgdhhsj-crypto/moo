// إنشاء قائمة السور
for(let i=1;i<=114;i++){
    const opt = document.createElement('option');
    opt.value = i;
    opt.textContent = surahNames[i];
    surahSelect.appendChild(opt);
}

// عرض الآيات عند اختيار السورة
surahSelect.addEventListener('change', () => {
    const surahNum = surahSelect.value;
    if(!surahNum) return;

    fetch(`https://api.alquran.cloud/v1/surah/${surahNum}`)
        .then(res => res.json())
        .then(data => {
            const ayahs = data.data.ayahs;
            let text = "";

            ayahs.forEach((a, i) => {
                // شيل أي بسملة موجودة داخل نص الآية
                let ayahTextClean = a.text.replace(/بسم الله الرحمن الرحيم/g, '').trim();
                text += `${i+1}. ${ayahTextClean} <br>`;
            });

            ayahText.innerHTML = text;
        })
        .catch(err => {
            ayahText.innerHTML = "الآيات غير متوفرة حالياً.";
            console.error(err);
        });
});

